Engine::Engine(const std::string& language) : hyphenator_(language) {}

std::string Engine::engineVersion() const {
    return "MarkdownPrintCore 0.1.0 (C++17, interop nativo)";
}

std::vector<Token> Engine::tokenize(const std::string& markdown) const {
    Lexer lexer;
    return lexer.tokenize(preprocessMarkdown(markdown));
}

std::vector<Block> Engine::parse(const std::string& markdown) const {
    ASTBuilder builder;
    auto preprocessed = preprocessMarkdown(markdown);
    auto refs = Lexer::parseReferences(preprocessed);
    Lexer lexer;
    return builder.build(lexer.tokenize(preprocessed), refs);
}

Layout Engine::layout(const std::string& markdown, double pageWidth, double pageHeight) const {
    auto blocks = parse(markdown);
    PageGeometry geometry = PageGeometry::standardMargins(pageWidth, pageHeight);
    ApproximateFontMetrics metrics;
    Typography typo;
    typo.textJustified = justifyText_;
    LayoutEngine engine(geometry, metrics, typo, &hyphenator_);
    return engine.layout(blocks);
}

MathLayoutInfo Engine::layoutMath(const std::string& latex, bool displayStyle, double fontSize) const {
    MathLayoutInfo result;
    auto ast = math::parseMath(latex, displayStyle);
    if (!ast) return result;

    math::MathLayoutEngine layoutEngine;
    auto layout = layoutEngine.layout(ast, fontSize, displayStyle);

    result.glyphs.reserve(layout.boxes.size());
    for (const auto& box : layout.boxes) {
        MathGlyphInfo info;
        info.x = box.x;
        info.y = box.y;
        info.width = box.width;
        info.height = box.height;
        info.depth = box.depth;
        info.fontSize = box.fontSize;
        info.isRule = box.isRule;
        info.isRadical = box.isRadical;
        info.unicode = box.unicode.empty() ? box.text : box.unicode;
        info.text = box.text;
        info.radicalContentHeight = box.radicalContentHeight;
        info.radicalContentWidth = box.radicalContentWidth;
        result.glyphs.push_back(std::move(info));
    }

    result.totalWidth = layout.totalWidth;
    result.totalHeight = layout.totalHeight;
    result.totalDepth = layout.totalDepth;
    result.baselineY = layout.baselineY;
    return result;
}

